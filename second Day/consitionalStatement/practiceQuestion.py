# Grade students based on marks
# marks >= 90, grade = "A"
# 90 > marks >= 80, grade = "B"
# 80 > marks >= 70, grade = "C"
# 70 > marks, grade = "D"
# __________________________________________________

# __________________________________________________


grade = int(input("enter your Grade : "))

if grade >= 90:
	print("Grade: A")
elif 90>= grade >=80:
	print("Grade: B") 
elif 80>= grade >=70:
	print("Grade: C")
elif 70>= grade >=60:
	print("Grade: D")
else:
	print("You are Failed")
	
